<h2 align="center">
  Topics in Logic: Computability Theory
</h2>

<p>
This class was run at the University of Colorado Boulder in the Spring
of 2011.  It was a class for advanced graduate students.
</p>

<p>
We studied the basics of computability theory with an aim to
finish with some nice priority arguments.  We used a draft of the
<a href="http://www.people.cs.uchicago.edu/~soare/cta/">new book</a>
by Robert Soare; this was distributed during the first week of
classes.
</p>

